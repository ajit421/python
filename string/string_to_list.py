chai = "Lemon, Ginger, Masala, Green, Mint"
print(chai) # Output: 'Lemon, Ginger, Masala, Green, Mint'

print(chai.split()) # Output: ['Lemon,', 'Ginger,', 'Masala,', 'Green,', 'Mint']

print(chai.split(',')) # Output: ['Lemon', ' Ginger', ' Masala', ' Green', ' Mint']

print(chai.split(', ')) # Output: ['Lemon', 'Ginger', 'Masala', 'Green', 'Mint']

