a=5
b=10
print(a,b)

# Output:
# 5 10

# c=a
# a=b
# b=c

a,b=b,a
print(a,b)

# Output:
# 10 5


# Explanation:
# In the above code, we have two variables a and b with values 5 and 10 respectively.
# We are printing the values of a and b.
# Then we are swapping the values of a and b using a,b=b,a.
# After swapping, we are printing the values of a and b.
# The output of the code is:
# 5 10
# 10 5
# In the first print statement, the values of a and b are printed as 5 and 10 respectively.
# In the second print statement, the values of a and b are printed as 10 and 5 respectively after swapping.
# So, the values of a and b are swapped successfully.