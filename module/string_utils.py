# reversal of a string 
def rev(string):
    return string[::-1]


# palidrome check
def palidrome(string):
    pal = string[::-1]
    return pal == string


# concatenation of two strinngs
def concatenation(string1, string2):
    return string1 + string2

# string length
def length(string):
    return len(string)

