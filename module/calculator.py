# for square
def square(num):
    return num * num

# for cube
def cube(num):
    return num**3

# for factorial
def factorial(num):
    if num == 0 or num == 1:
        return 1
    else:
        return num * factorial(num-1)

# for divide
def divide(num1, num2):
    return num1 / num2

# for addition
def add(num1, num2):
    return num1 + num2

# for substraction
def sub(num1, num2):
    return num1 - num2

# for multiplication
def multiplication(num1, num2):
    return num1 * num2

