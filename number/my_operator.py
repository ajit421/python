# Arithmetic Addition
x, y, z = 5, 10, 15
print("Sum:", x + y + z)  # Output: 30

# String Concatenation
name1 = 'Ajit' + 'Kumar'  # Without space
name2 = 'Ajit' + ' Kumar'  # With space

print("Without Space:", name1)   # Output: 'AjitKumar'
print("With Space:", name2)      # Output: 'Ajit Kumar'

# Alternative String Concatenation Methods
first, last = "Ajit", "Kumar"

print(first, last) # Output: 'Ajit Kumar'
print(first + last) # Output: 'AjitKumar'
print(first, last, sep='')  # Output: 'AjitKumar'
print(first, last, sep=' ')  # Output: 'Ajit Kumar'
print(first, last, sep='|')  # Output: 'Ajit|Kumar'



